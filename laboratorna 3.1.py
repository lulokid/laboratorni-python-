# Отримати кожний 4-й символ починаючи від 19-го символа і до кінця.
t = str(input("Введіть слово чи речення, у якому більше за 19 символів: "))

while (len(t) < 19):

    t = str(input("Введіть ще раз, так як кількість символів менша за 19: "))

print("Після зрізу кожного 4-го символу від 19-го і до кінця: ", t[18::4])